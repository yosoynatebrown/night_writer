module BrailleLookupable

  def punctuation_hash
    {
     " " => ["..", "..", ".."],
     "!" => ["..", "00", "0."],
     "'" => ["..", "..", "0."],
     "," => ["..", "0.", ".."],
     "-" => ["..", "..", "00"],
     "." => ["..", "00", ".0"],
     "?" => ["..", "0.", "00"]
   }
  end

  def lower_case_hash
    {
     "a" => ["0.", "..", ".."],
     "b" => ["0.", "0.", ".."],
     "c" => ["00", "..", ".."],
     "d" => ["00", ".0", ".."],
     "e" => ["0.", ".0", ".."],
     "f" => ["00", "0.", ".."],
     "g" => ["00", "00", ".."],
     "h" => ["0.", "00", ".."],
     "i" => [".0", "0.", ".."],
     "j" => [".0", "00", ".."],
     "k" => ["0.", "..", "0."],
     "l" => ["0.", "0.", "0."],
     "m" => ["00", "..", "0."],
     "n" => ["00", ".0", "0."],
     "o" => ["0.", ".0", "0."],
     "p" => ["00", "0.", "0."],
     "q" => ["00", "00", "0."],
     "r" => ["0.", "00", "0."],
     "s" => [".0", "0.", "0."],
     "t" => [".0", "00", "0."],
     "u" => ["0.", "..", "00"],
     "v" => ["0.", "0.", "00"],
     "w" => [".0", "00", ".0"],
     "x" => ["00", "..", "00"],
     "y" => ["00", ".0", "00"],
     "z" => ["0.", ".0", "00"]
   }
  end

  def number_hash
    {
      "1" => [".00.", ".0..", "00.."],
      "2" => [".00.", ".00.", "00.."],
      "3" => [".000", ".0..", "00.."],
      "4" => [".000", ".0.0", "00.."],
      "5" => [".00.", ".0.0", "00.."],
      "6" => [".000", ".00.", "00.."],
      "7" => [".000", ".000", "00.."],
      "8" => [".00.", ".000", "00.."],
      "9" => [".0.0", ".00.", "00.."],
      "0" => [".0.0", ".000", "00.."]
    }
  end

  def braille_hash
    upcase_hash = Hash.new([])

    lower_case_hash.each do |char, array|
      upcase_hash[char.upcase] = ".." + array[0], ".." + array[1], ".0" + array[2]
    end

    number_hash.merge(punctuation_hash.merge(lower_case_hash.merge(upcase_hash)))
  end

end
